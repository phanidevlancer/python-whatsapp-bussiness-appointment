export type AppThemeId = (typeof appThemes)[number]['id'];

export const defaultThemeId: AppThemeId = 'ora-default';

const appThemeMap = new Map<AppThemeId, AppTheme>(
  appThemes.map((theme) => [theme.id, theme] as const),
);

const defaultTheme = appThemeMap.get(defaultThemeId) ?? appThemes[0];

function isAppThemeId(themeId: string): themeId is AppThemeId {
  return appThemeMap.has(themeId as AppThemeId);
}

export function getThemeById(themeId: AppThemeId): AppTheme;
export function getThemeById(themeId: string): AppTheme;
export function getThemeById(themeId: AppThemeId | string): AppTheme {
  if (!isAppThemeId(themeId)) {
    return getThemeByIdOrDefault(themeId);
  }

  const theme = appThemeMap.get(themeId);
  if (!theme) {
    throw new Error(`Unknown theme id: ${themeId}`);
  }

  return theme;
}

export function getThemeByIdOrDefault(themeId: string): AppTheme {
  return isAppThemeId(themeId) ? getThemeById(themeId) : defaultTheme;
}

export function getThemeTokens(themeId: AppThemeId, appearance: ThemeAppearance) {
  return getThemeById(themeId).modes[appearance].tokens;
}

export function getThemeTokensOrDefault(themeId: string, appearance: ThemeAppearance) {
  return getThemeByIdOrDefault(themeId).modes[appearance].tokens;
}
