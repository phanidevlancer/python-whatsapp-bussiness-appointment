export type ThemeTokenName = (typeof themeTokenNames)[number];
export type ThemeTokens = Record<ThemeTokenName, string>;
export type ThemeAppearance = 'light' | 'dark';
export type ThemeAppearancePreference = ThemeAppearance | 'system';

export interface ThemeMode {
  tokens: ThemeTokens;
}

export interface AppTheme {
  id: AppThemeId;
  label: string;
  description: string;
  modes: Record<ThemeAppearance, ThemeMode>;
}

function makeMode(tokens: ThemeTokens): ThemeMode {
  return { tokens };
}

function makeTheme(
  id: AppThemeId,
  label: string,
  description: string,
  lightTokens: ThemeTokens,
  darkTokens: ThemeTokens,
): AppTheme {
  return {
    id,
    label,
    description,
    modes: {
      light: makeMode(lightTokens),
      dark: makeMode(darkTokens),
    },
  };
}

